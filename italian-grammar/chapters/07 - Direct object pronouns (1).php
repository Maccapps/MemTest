<h3>What if there is more than one object?</h3>
<p>The same thing as when an adjective refers to two or more nouns that are different in gender, it has the masculine plural form</p>

<div class="row">
    <div class="col-xs-12 col-sm-6 en">- "Do you know Laura and Piero?" <br/> &nbsp; "Yes. I know <b>them</b> well."</div>
    <div class="col-xs-12 col-sm-6 it">- "Conosci Laura e Piero ?" <br/> &nbsp; "Si, <b>li</b> conosco bene."</div>
</div>

<div class="row">
    <div class="col-xs-12 col-sm-6 en">- "Do you use the computer and the printer?" <br/> &nbsp; "Yes, I often use <b>them</b>."</div>
