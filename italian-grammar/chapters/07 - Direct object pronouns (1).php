<h3>What is a direct object?</h3>
<p>A direct object is the person or thing that the verb directly impacts on:</p>
<div class="row">
    <div class="col-xs-12 col-sm-6 en">- I'm inviting <b>Elena</b></div>
    <div class="col-xs-12 col-sm-6 it">- Invito <b>Elena</b></div>
</div>
<div class="row">
    <div class="col-xs-12 col-sm-6 en">- I use the <b>computer</b></div>
    <div class="col-xs-12 col-sm-6 it">- Uso il <b>computer</b> tutti i giorni</div>
</div>
<br/>
<p>A direct object pronoun replaces a noun used as a direct object; it must therefore agree in gender and number with the noun it refers to. Direct object pronouns normally come before the verb. In the following examples, the pronouns are in italics:</p>
<div class="row">
    <div class="col-xs-12 col-sm-6 en">- When I see Elena I'll invite <b>her</b> to dinner.</div>
    <div class="col-xs-12 col-sm-6 it">- Quando vedo Elena <b>la</b> invito a cena.</div>
</div>
<div class="row">
    <div class="col-xs-12 col-sm-6 en">- When I see Lorenzo I'll invite <b>him</b> to dinner.</div>
    <div class="col-xs-12 col-sm-6 it">- Quando vedo Lorenzo <b>lo</b> invito a cena.</div>
</div>
<div class="row">
    <div class="col-xs-12 col-sm-6 en">- I write the postcards and then post <b>them</b>.</div>
    <div class="col-xs-12 col-sm-6 it">- Scrivo le cartoline e poi <b>le</b> imbuco.</div>
</div>
<div class="row">
    <div class="col-xs-12 col-sm-6 en">- As soon as I buy biscuits I eat <b>them</b>.</div>
    <div class="col-xs-12 col-sm-6 it">- Appena compro i biscotti <b>li</b> mangio.</div>
</div>
<div class="row">
    <div class="col-xs-12 col-sm-6 en">- I get the paper and read <b>it</b> immediately.</div>
    <div class="col-xs-12 col-sm-6 it">- Prendo il giornale e <b>lo</b> leggo subito.</div>
</div>



